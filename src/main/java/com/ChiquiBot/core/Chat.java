package com.ChiquiBot.core;

public class Chat {
    private int id;
    private boolean is_bot;
    private String first_name;
    private String last_name;
    private String type;

    public int getId() {
        return id;
    }

    public boolean isIs_bot() {
        return is_bot;
    }

    public String getFirst_name() {
        return first_name;
    }

    public String getLast_name() {
        return last_name;
    }

    public String getType() {
        return type;
    }
}
